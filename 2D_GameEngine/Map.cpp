Map::Map(SDL_Renderer* ren)
{
	renderer = ren;

	dirt = TextureManager::LoadTexture("C:/Dev/2D_GameEngine/2D_GameEngine/assets/dirt.png", renderer);
	grass = TextureManager::LoadTexture("C:/Dev/2D_GameEngine/2D_GameEngine/assets/grass.png", renderer);
	water = TextureManager::LoadTexture("C:/Dev/2D_GameEngine/2D_GameEngine/assets/water.png", renderer);

	LoadMap(lvl1);

	src.x = src.y = 0;
	dest.x = dest.y = 0;
	src.w = dest.w = 32;
	src.h = dest.h = 32;

}

void Map::LoadMap(int arr[20][25])
{
	for (int row = 0; row < 20; row++)
	{
		for (int column = 0; column < 25; column++)
		{
			map[row][column] = arr[row][column];
		}
	}
}

void Map::DrawMap()
{
	int type = 0;

	for (int row = 0; row < 20; row++)
	{
		for (int column = 0; column < 25; column++)
		{
			type = map[row][column];

			dest.x = column * 32;
			dest.y = row * 32;

			switch (type)
			{
			case 0:
				TextureManager::Draw(renderer, water, src, dest);
				break;
			case 1:
				TextureManager::Draw(renderer, grass, src, dest);
				break;
			case 2:
				TextureManager::Draw(renderer, dirt, src, dest);
				break;
			}
		}
	}
}